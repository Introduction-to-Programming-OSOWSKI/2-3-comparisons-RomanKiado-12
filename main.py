def greaterThan(x, y):
    if y > x:
        return "true"
    else:
        return "false"
def lessThan(x, y):
    if y < x:
        return "true"
    else: return "false"
def equalTo(x, y):
    if y == x:
        return "true"
    else: return "false"
def greaterOrEqual(x, y):
    if y >= x:
        return "true"
    else: 
        return "false"
def lessOrEqual(x, y):
    if y <= x:
        return "true"
    else:
        return "false"
